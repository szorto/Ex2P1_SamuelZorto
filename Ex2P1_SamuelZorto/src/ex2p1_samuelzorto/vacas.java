package ex2p1_samuelzorto;


public class vacas {
    public String nomb;
    public int precio;

    public vacas(String nomb) {
        this.nomb = nomb;
        this.precio = 350;
    }

    public String getNomb() {
        return nomb;
    }

    public void setNomb(String nomb) {
        this.nomb = nomb;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
    
    
}
